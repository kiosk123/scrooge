package vo;

//JSON 통신시 year와 month 프로퍼티를 매핑시키기 위한 VO
public class YearAndMonth {
	Integer year;
	Integer month;
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
}
